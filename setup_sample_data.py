def setup():
    init_db()
    db = get_db()
    try:
        # 카테고리 ID 매핑
        categories = {}
        for row in db.execute("SELECT id, name FROM categories").fetchall():
            categories[row['name']] = row['id']

        inserted = 0
        for item in SAMPLE_PRODUCTS:
            name, cat_name, unit, price, cost, stock, min_stock, bulk_th, bulk_pr, barcode = item
            cat_id = categories.get(cat_name)

            existing = db.execute("SELECT id FROM products WHERE barcode = ?", (barcode,)).fetchone()
            if existing:
                continue

            db.execute("""
                INSERT INTO products (barcode, name, category_id, unit_type, price, cost_price,
                    stock_quantity, min_stock, bulk_threshold, bulk_price)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            """, (barcode, name, cat_id, unit, price, cost, stock, min_stock, bulk_th, bulk_pr))
            inserted += 1

        db.commit()
        print(f"샘플 데이터 {inserted}개 상품 등록 완료!")
        print(f"총 상품 수: {db.execute('SELECT COUNT(*) FROM products').fetchone()[0]}")
    finally:
        db.close()


if __name__ == '__main__':
    setup()
